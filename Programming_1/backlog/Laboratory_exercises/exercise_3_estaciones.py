# ESTACIONES
# Un profesor desea contar con un programa que le permita saber los porcentajes de alumnos de su sección
# que han nacido en cada una de las estaciones del año.
# Para ello se pide que elabore un programa que:
# - Lea como dato la cantidaa de alumnos, dato que debe ser mayor a 5 y menor a 41. El programa debe validar
# el ingreso de esta información.
# - Permita a cada alumno ingresar el día y el mes en que nació. No es necesario validar el día y mes, se
# trabajará bajo la premisa que cada alumno ingresará datos correctos. Con esta información se realizará el
# cálculo de los porcentajes de nacimiento en cada estación de año.
# - Finalmente, el programa mostrará los porcentajes.

# Se le recuerda que las estaciones estan marcadas de acuerdo a las fechas que se indican en la siguiente tabla:

# |  ESTACIÓN  |  INICIO        |  FIN           |
# |  Verano    |  21 Diciembre  |  20 Marzo      |
# |  Otoño     |  21 Marzo      |  21 Junio      |
# |  Invierno  |  22 Junio      |  22 Setiembre  |
# | Primavera  |  23 Setiembre  |  20 Diciembre  |

