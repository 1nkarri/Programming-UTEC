number_month = int(input("Ingrese el número del mes: "))

if number_month == 1:
    print("Enero")
elif number_month == 2:
    print("Febrero")
elif number_month == 3:
    print("Marzo")
elif number_month == 4:
    print("Abril")
elif number_month == 5:
    print("Mayo")
elif number_month == 6:
    print("Junio")
elif number_month == 7:
    print("Julio")
elif number_month == 8:
    print("Agosto")
elif number_month == 9:
    print("Septiembre")
elif number_month == 10:
    print("Octubre")
elif number_month == 11:
    print("Noviembre")
elif number_month == 12:
    print("Diciembre")
else:
    print("No existe ese número del mes")